"""
Утилиты для работы с регистрационными номерами.
"""

from __future__ import annotations


def format_doc_no(numeric: int) -> str:
    """
    Форматирует числовой номер в строку вида `УТЗ-000123`.

    Число всегда дополняется ведущими нулями до 6 знаков.

    Args:
        numeric: Числовое значение регистрационного номера.

    Returns:
        str: Номер в формате УТЗ-XXXXXX.
    """
    return f"УТЗ-{str(numeric).zfill(6)}"


def is_golden(numeric: int) -> bool:
    """
    Проверяет, является ли номер "золотым" (кратен 100).

    Используется для применения особых бизнес-правил — обычные пользователи
    не могут резервировать и назначать золотые номера.

    Args:
        numeric: Числовое значение номера.

    Returns:
        bool: True, если номер заканчивается на 00.
    """
    return numeric % 100 == 0
