"""
Модуль общих (shared) Pydantic-схем.

Содержит универсальные модели данных, которые переиспользуются 
в различных частях API для унификации ответов и параметров запросов:

- Простые текстовые сообщения об успехе/статусе операции
- Ответы при создании сущностей (возврат ID)
- Параметры пагинации с защитой от чрезмерной нагрузки

Использование этих схем помогает поддерживать единообразие контрактов API.
"""

from __future__ import annotations

from pydantic import BaseModel, Field


class Message(BaseModel):
    """
    Универсальная схема для возврата простых текстовых сообщений.

    Используется в эндпоинтах, где достаточно вернуть статус операции
    или информационное сообщение без дополнительных данных
    (например, после завершения сессии, отмены действия и т.п.).
    """
    message: str = Field(..., description="Текст сообщения или описание результата операции")


class IDResponse(BaseModel):
    """
    Стандартный ответ при успешном создании новой записи.

    Возвращает идентификатор созданной сущности. Используется в POST-эндпоинтах,
    где клиенту необходимо сразу получить ID нового объекта.
    """
    id: int | str = Field(..., description="Идентификатор созданной записи (может быть int или UUID в виде строки)")


class Pagination(BaseModel):
    """
    Схема параметров пагинации (offset/limit).

    Используется в эндпоинтах, возвращающих списки данных.
    Содержит встроенную валидацию для предотвращения злоупотреблений:
    максимальное значение `limit` ограничено 500 записями.
    """
    limit: int = Field(
        default=20,
        ge=1,
        le=500,
        description="Количество записей на странице (от 1 до 500)"
    )
    offset: int = Field(
        default=0,
        ge=0,
        description="Смещение от начала выборки (количество пропущенных записей)"
    )
