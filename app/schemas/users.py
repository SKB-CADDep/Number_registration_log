"""
Модуль Pydantic-схем для работы с пользователями.

Содержит модели данных, связанные с пользователями системы, 
используемые в ответах API. В настоящий момент включает только 
схему публичной информации о пользователе.
"""

from pydantic import BaseModel, ConfigDict, Field


class UserInResponse(BaseModel):
    """
    Схема представления данных пользователя в ответах API.

    Содержит только публичную (нечувствительную) информацию о пользователе.
    Используется в эндпоинтах, где требуется вернуть данные текущего пользователя
    или автора документа (например, `/users/me`, отчеты, административные списки).

    Примечание: если в будущем потребуется больше информации (is_admin, department и т.д.),
    рекомендуется расширить эту схему или создать отдельную `CurrentUser`.
    """
    id: int = Field(..., description="Внутренний идентификатор пользователя в базе")
    username: str = Field(..., description="Логин пользователя (из Active Directory / LDAP)")

    model_config = ConfigDict(from_attributes=True)

